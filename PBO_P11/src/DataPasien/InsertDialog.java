    private void jAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAddActionPerformed
      if (tfID.getText().isEmpty() || tfNama.getText().isEmpty() || tfTL.getText().isEmpty() ||
            tfAlamat.getText().isEmpty() || tfNoHP.getText().isEmpty() || tfDiagnosa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua data harus diisi!");
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO_P11PU");
        EntityManager em = emf.createEntityManager();

        try {
            em.getTransaction().begin();

            PasienEntity pasien = new PasienEntity();
            pasien.setId_pasien(tfID.getText().trim());
            pasien.setNama_pasien(tfNama.getText().trim());
            pasien.setAlamat(tfAlamat.getText().trim());
            pasien.setDiagnosa(tfDiagnosa.getText().trim());

            // Parsing tanggal lahir
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date tglLahir;
            try {
                tglLahir = sdf.parse(tfTL.getText().trim());
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(null, "Format tanggal salah! Gunakan yyyy-MM-dd");
                return;
            }
            pasien.setTanggal_lahir(tglLahir);

            // Parsing nomor HP
            try {
                pasien.setNo_hp(Integer.parseInt(tfNoHP.getText().trim()));
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Nomor HP harus angka!");
                return;
            }

            em.persist(pasien);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");

            // Refresh tabel di parent JFrame
            if (parentFrame != null) {
                parentFrame.showTable();
            }


            dispose();

        } catch (Exception e) {
            if (em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(null, "Gagal menyimpan data: " + e.getMessage());
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_jAddActionPerformed

    
